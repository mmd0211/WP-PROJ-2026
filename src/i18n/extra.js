const EXTRA_FA_TO_EN = Object.fromEntries(Object.entries(EXTRA_EN_TO_FA).map(([en, fa]) => [fa, en]));
const PERSIAN_DIGITS = '۰۱۲۳۴۵۶۷۸۹';
const toFa = (value) => value.replace(/\d/g, (digit) => PERSIAN_DIGITS[Number(digit)]);
const toEn = (value) => value.replace(/[۰-۹]/g, (digit) => String(PERSIAN_DIGITS.indexOf(digit)));

function translateExtraDynamic(value, language) {
  let match;
  if (language === 'fa') {
    if ((match = value.match(/^(\d+) followers$/))) return `${toFa(match[1])} دنبال‌کننده`;
    if ((match = value.match(/^(\d+) releases$/))) return `${toFa(match[1])} اثر`;
    if ((match = value.match(/^Audio file “(.+)” is ready for the mock application\.$/))) return `فایل صوتی «${match[1]}» برای نسخه نمایشی آماده است.`;
    if ((match = value.match(/^Delete release “(.+)”\?$/))) return `اثر «${match[1]}» حذف شود؟`;
    if ((match = value.match(/^Ticket #(\d+)$/))) return `تیکت شماره ${toFa(match[1])}`;
    if ((match = value.match(/^Basic: (\d+)$/))) return `پایه: ${toFa(match[1])}`;
    if ((match = value.match(/^Silver: (\d+)$/))) return `نقره‌ای: ${toFa(match[1])}`;
    if ((match = value.match(/^Gold: (\d+)$/))) return `طلایی: ${toFa(match[1])}`;
  } else {
    if ((match = value.match(/^([۰-۹]+) دنبال‌کننده$/))) return `${toEn(match[1])} followers`;
    if ((match = value.match(/^([۰-۹]+) اثر$/))) return `${toEn(match[1])} releases`;
  }
  return value;
}

export function translateExtraText(rawValue, language = 'en') {
  if (rawValue === null || rawValue === undefined) return rawValue;
  const raw = String(rawValue);
  if (!raw.trim()) return raw;
  const leading = raw.match(/^\s*/)?.[0] || '';
  const trailing = raw.match(/\s*$/)?.[0] || '';
  const value = raw.trim();
  if (/^(https?:\/\/|\/|[\w.+-]+@[\w.-]+\.[A-Za-z]{2,})/.test(value)) return raw;
  const translated = language === 'fa'
    ? (EXTRA_EN_TO_FA[value] || translateExtraDynamic(value, language))
    : (EXTRA_FA_TO_EN[value] || translateExtraDynamic(value, language));
  return `${leading}${translated}${trailing}`;
}

export function localizeExtraDocument(language = 'en', root = document.body) {
  if (!root) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    const parent = node.parentElement;
    if (!parent || ['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE'].includes(parent.tagName) || parent.closest('[data-no-i18n]')) return;
    const translated = translateExtraText(node.data, language);
    if (translated !== node.data) node.data = translated;
  });
  root.querySelectorAll?.('[placeholder], [title], [aria-label], [alt]').forEach((element) => {
    if (element.closest('[data-no-i18n]')) return;
    ['placeholder', 'title', 'aria-label', 'alt'].forEach((attribute) => {
      if (!element.hasAttribute(attribute)) return;
      const current = element.getAttribute(attribute);
      const translated = translateExtraText(current, language);
      if (translated !== current) element.setAttribute(attribute, translated);
    });
  });
}
