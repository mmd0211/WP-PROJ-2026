  if ((match = value.match(/^Play (.+)$/))) return `پخش ${EN_TO_FA[match[1]] || match[1]}`;
  if ((match = value.match(/^Remove (.+) from queue$/))) return `حذف ${EN_TO_FA[match[1]] || match[1]} از صف پخش`;
  if ((match = value.match(/^Repeat: (off|all|one)$/))) return `تکرار: ${{ off: 'خاموش', all: 'همه', one: 'یکی' }[match[1]]}`;
