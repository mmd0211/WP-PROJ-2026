function translateDynamicPersian(value) {
  let match;
  if ((match = value.match(/^سلام (.+) 👋$/))) return `Hello ${match[1]} 👋`;
  if ((match = value.match(/^([۰-۹]+) پخش امروز$/))) return `${toEnglishDigits(match[1])} streams today`;
  if ((match = value.match(/^([۰-۹]+) آهنگ$/))) return `${toEnglishDigits(match[1])} tracks`;
  if ((match = value.match(/^([۰-۹]+) شنونده$/))) return `${toEnglishDigits(match[1])} listeners`;
  if ((match = value.match(/^([۰-۹]+) پخش$/))) return `${toEnglishDigits(match[1])} streams`;
  if ((match = value.match(/^([۰-۹]+) نتیجه$/))) return `${toEnglishDigits(match[1])} results`;
  return value;
}

export function translateText(rawValue, language = 'en') {
  if (rawValue === null || rawValue === undefined) return rawValue;
  const raw = String(rawValue);
  if (!raw.trim()) return raw;

  const leading = raw.match(/^\s*/)?.[0] || '';
  const trailing = raw.match(/\s*$/)?.[0] || '';
  const value = raw.trim();

  // Do not touch URLs, emails, file names, or route-like technical values.
  if (/^(https?:\/\/|\/|[\w.+-]+@[\w.-]+\.[A-Za-z]{2,})/.test(value)) return raw;

  let translated;
  if (language === 'fa') {
    translated = EN_TO_FA[value] || translateDynamicEnglish(value);
    if (translated === value && /^[\d\s.,:%+\-/]+$/.test(value)) translated = toPersianDigits(value);
  } else {
    translated = FA_TO_EN[value] || translateDynamicPersian(value);
    if (translated === value && /^[۰-۹\s٫،٪٪+\-/]+$/.test(value)) translated = toEnglishDigits(value).replaceAll('٪', '%').replaceAll('،', ',');
  }
  return `${leading}${translated}${trailing}`;
}

const SKIP_TAGS = new Set(['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE']);
const TRANSLATABLE_ATTRIBUTES = ['placeholder', 'title', 'aria-label', 'alt'];

export function localizeDocument(language = 'en', root = document.body) {
  if (!root) return;

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const textNodes = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode);

  textNodes.forEach((node) => {
    const parent = node.parentElement;
    if (!parent || SKIP_TAGS.has(parent.tagName) || parent.closest('[data-no-i18n]')) return;
    const translated = translateText(node.data, language);
    if (translated !== node.data) node.data = translated;
  });

  const elements = root.querySelectorAll?.('[placeholder], [title], [aria-label], [alt]') || [];
  elements.forEach((element) => {
    if (element.closest('[data-no-i18n]')) return;
    TRANSLATABLE_ATTRIBUTES.forEach((attribute) => {
      if (!element.hasAttribute(attribute)) return;
      const current = element.getAttribute(attribute);
      const translated = translateText(current, language);
      if (translated !== current) element.setAttribute(attribute, translated);
    });
  });
}

export function formatLocaleNumber(value, language = 'en') {
  return Number(value).toLocaleString(language === 'fa' ? 'fa-IR' : 'en-US');
}

export function getTextDirection(language = 'en') {
  return language === 'fa' ? 'rtl' : 'ltr';
}
